///////////////////////////////////////////////////////////////////////////////////////////

#include "SpellEffect.h"

///////////////////////////////////////////////////////////////////////////////////////////

// CONSTRUCTOR.
SpellEffect::SpellEffect()
{
	initSpellEffect();
}

// DESTRUCTOR.
SpellEffect::~SpellEffect()
{
	delete spell;
	spell = nullptr;
}

///////////////////////////////////////////////////////////////////////////////////////////

// FUNCTIONS.
void SpellEffect::render()
{
	spell->render();
}

///////////////////////////////////////////////////////////////////////////////////////////

void SpellEffect::initSpellEffect()
{
	spell = new Disc(20.0f, 0.5f, m_spellTexPath, false, 0);
}

